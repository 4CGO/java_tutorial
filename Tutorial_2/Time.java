package Tutorial_2;

// Program Time prints hours in seconds.
public class Time {
    public static final double SECONDS = 60;

    public static void main(String[] args) {
        System.out.println("One hour is " + (SECONDS * 1) + " seconds.");
        System.out.println("Ten hours is " + (SECONDS * 10) + "seconds.");
        System.out.println("One hundred hours is " + (SECONDS * 100) + " seconds.");
    }
}
